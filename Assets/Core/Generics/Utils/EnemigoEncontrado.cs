using System;
using UnityEngine;
using Assets.Core.Features.Enemies.Models;

namespace Assets.Core.Generics.Utils
{
    public class EnemigoEncontrado : MonoBehaviour
    {
        /* ================================================================================================================
        ---------------------------------------------------- CAMPOS -----------------------------------------------------
        ================================================================================================================= */
        [SerializeField] private EnemyData _enemyData;

        /* ================================================================================================================
        ---------------------------------------------------- EVENTOS -----------------------------------------------------
        ================================================================================================================= */
        public static event Action<EnemyData> OnEnemigoEncontrado;

        /* ================================================================================================================
        ---------------------------------------------------- MÉTODOS DE UNITY -----------------------------------------------------
        ================================================================================================================= */ 
        private void OnTriggerEnter(Collider other)
        {
            if (other.CompareTag("Player"))
            {
                OnEnemigoEncontrado?.Invoke(_enemyData);
            }
        }
    }
}